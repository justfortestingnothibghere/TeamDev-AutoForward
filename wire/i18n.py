def set_user_lang_cache(uid: int, lang: str) -> None:
    if lang in LANGUAGES:
        _user_lang_cache[uid] = lang


def get_user_lang_cache(uid: int) -> Optional[str]:
    return _user_lang_cache.get(uid)


def t(key: str, lang: str = DEFAULT_LANG) -> str:
    entry = _T.get(key)
    if entry is None:
        return key
    return entry.get(lang) or entry.get(DEFAULT_LANG) or key


